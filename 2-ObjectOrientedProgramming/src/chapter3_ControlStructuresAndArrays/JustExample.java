package chapter3_ControlStructuresAndArrays;

public class JustExample {
	public static void main(String[] args) {
		
		int intVal;
		float floatVal;
		double doubleVal;
		
		floatVal=(20/6f);
		
		doubleVal =(20/6d);
		intVal=20/6;
		
		System.out.println(floatVal);
		System.out.println(intVal);
		
		System.out.println(doubleVal);
		
	}

}
